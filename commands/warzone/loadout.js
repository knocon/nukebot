class Weapon {
    constructor(
        name,
        type,
        muzzle,
        barrel,
        laser,
        optic,
        stock,
        underbarrel,
        ammunition,
        magazine,
        reargrip,
        triggeraction,
    ) {
        this.name = name;
        this.type = type;
        this.muzzle = muzzle;
        this.barrel = barrel;
        this.laser = laser;
        this.optic = optic;
        this.stock = stock;
        this.underbarrel = underbarrel;
        this.ammunition = ammunition;
        this.magazine = magazine;
        this.reargrip = reargrip;
        this.triggeraction = triggeraction;
    }
}
